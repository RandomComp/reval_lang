#!/bin/bash

# test_reval.sh — МАКСИМАЛЬНО РАСШИРЕННЫЕ тесты для reval

REVAL="./reval_linux_x86"
TMP_OUT="test_output.tmp"
TMP_ERR="test_error.tmp"

GREEN="\033[32m"
RED="\033[31m"
YELLOW="\033[33m"
RESET="\033[0m"

PASSED=0
FAILED=0
FAILED_TESTS=()
FAILED_EXPR=()

run_test() {
    local expr="$1"
    local expected="$2"
    local description="$3"
    local expect_error="$4"

    echo -n "Ожидается $expr = $expected ... "

    "$REVAL" "$expr" > "$TMP_OUT" 2> "$TMP_ERR"
    local result=$?
    local output=$(cat "$TMP_OUT" | tr -d '\n\r')
    local error_output=$(cat "$TMP_ERR" | sed '/^$/d')

    if [ -n "$expect_error" ]; then
        if [ $result -ne 0 ] || [ -n "$error_output" ]; then
            echo -e "${GREEN}✓${RESET}"
            ((PASSED++))
        else
            echo -e "${RED}✗${RESET}"
            FAILED_TESTS+=("$description")
            FAILED_EXPR+=("$expr")
            ((FAILED++))
            echo "  Ожидалась ошибка, но получен код 0 и stderr пуст"
        fi
    else
        if [ "$output" == "$expected" ]; then
            echo -e "${GREEN}✓${RESET}"
            ((PASSED++))
        else
            echo -e "${RED}✗${RESET}"
            FAILED_TESTS+=("$description")
            FAILED_EXPR+=("$expr")
            ((FAILED++))
            echo "  Ожидалось: $expected"
            echo "  Получено:  $output"
            [ -n "$error_output" ] && echo "  stderr: $error_output"
        fi
    fi
}

if [ ! -f "$REVAL" ]; then
    echo "Не найден $REVAL, запусти make"
    exit 1
fi

echo -e "${YELLOW}=== МАКСИМАЛЬНОЕ тестирование reval (с присваиваниями и инкрементами) ===${RESET}"
echo ""

# --- Базовые операции ---
run_test "2 + 2" "4" "Сложение"
run_test "10 - 3" "7" "Вычитание"
run_test "3 * 4" "12" "Умножение"
run_test "12 / 3" "4" "Деление"
run_test "10 % 3" "1" "Остаток от деления"
run_test "2 ** 10" "1024" "Возведение в степень"

# --- Составные присваивания ---
run_test "x = 5; x += 3; x" "8" "Присваивание с +="
run_test "x = 10; x -= 4; x" "6" "Присваивание с -="
run_test "x = 3; x *= 4; x" "12" "Присваивание с *="
run_test "x = 12; x /= 3; x" "4" "Присваивание с /="
run_test "x = 7; x %= 3; x" "1" "Присваивание с %="
run_test "x = 2; x **= 10; x" "1024" "Присваивание с **="

# --- Составные присваивания с выражениями ---
run_test "x = 5; x += 2 * 3; x" "11" "+= с выражением"
run_test "x = 10; x -= 20 / 4; x" "5" "-= с выражением"
run_test "x = 3; x *= 2 + 2; x" "12" "*= с выражением"
run_test "x = 12; x /= 2 + 1; x" "4" "/= с выражением"

# --- Префиксный инкремент/декремент ---
run_test "x = 5; ++x; x" "6" "Префиксный ++"
run_test "x = 5; --x; x" "4" "Префиксный --"
run_test "x = 5; ++x + 10" "16" "Префиксный ++ в выражении"
run_test "x = 5; --x + 10" "14" "Префиксный -- в выражении"
run_test "x = 5; y = ++x; y" "6" "Префиксный ++ при присваивании"
run_test "x = 5; y = --x; y" "4" "Префиксный -- при присваивании"

# --- Постфиксный инкремент/декремент ---
run_test "x = 5; x++; x" "6" "Постфиксный ++ (значение после)"
run_test "x = 5; x--; x" "4" "Постфиксный -- (значение после)"
run_test "x = 5; x++ + 10" "15" "Постфиксный ++ в выражении (старое значение)"
run_test "x = 5; x-- + 10" "15" "Постфиксный -- в выражении (старое значение)"
run_test "x = 5; y = x++; y" "5" "Постфиксный ++ при присваивании (старое значение)"
run_test "x = 5; y = x--; y" "5" "Постфиксный -- при присваивании (старое значение)"

# --- Смешанные инкременты/декременты ---
run_test "x = 5; y = ++x + x++ + --x + x--; y" "24" "Смесь префиксных и постфиксных"
run_test "x = 1; y = x++ + ++x; y" "4" "Постфиксный и префиксный вместе"

# --- Инкременты/декременты в сложных выражениях ---
run_test "x = 10; y = 5; z = x++ - --y; z" "6" "Комбинация"
run_test "x = 3; y = x++ * ++x; y" "15" "Умножение с инкрементами"
run_test "x = 4; y = x-- * --x; y" "8" "Умножение с декрементами"

# --- Цепочки присваиваний ---
run_test "x = y = z = 5; x + y + z" "15" "Цепочка присваиваний"
run_test "x = y = 5; x += y += 2; x" "12" "Цепочка += в присваивании"
run_test "x = 5; y = x += 3; y" "8" "Присваивание результата составной операции"

# --- Инкременты/декременты с присваиваниями ---
run_test "x = 5; x = ++x + x++ + ++x; x" "20" "Сложный тест"
run_test "x = 5; x += ++x; x" "11" "+= с префиксным инкрементом"
run_test "x = 5; x -= x--; x" "0" "-= с постфиксным декрементом"

# --- Составные присваивания с неопределёнными переменными ---
run_test "x += 5" "" "+= с неопределённой переменной" "1"
run_test "x *= 2" "" "*= с неопределённой переменной" "1"
run_test "++x" "" "Префиксный ++ с неопределённой переменной" "1"
run_test "x++" "" "Постфиксный ++ с неопределённой переменной" "1"

# --- Приоритет операторов с инкрементами ---
run_test "x = 5; 10 * ++x" "60" "Умножение и префиксный ++"
run_test "x = 5; 10 * x++" "50" "Умножение и постфиксный ++"

# --- Ошибки при переполнении ---
run_test "x = 2147483647; ++x" "-2147483648" "Переполнение при префиксном ++"
run_test "x = -2147483648; --x" "2147483647" "Переполнение при префиксном --"
run_test "x = 2147483647; x++" "2147483647" "Постфиксный ++ возвращает старое значение (переполнение позже)"

# --- Научная нотация и степени ---
run_test "1e5" "100000" "Научная нотация (десятичная)"
run_test "0b1e10" "1024" "Научная нотация (бинарная)"
run_test "0x1p10" "1048576" "Научная нотация (шестнадцатеричная)"
run_test "0x1e10" "7696" "Hex-число с e (не научная нотация)"

# --- Проверка работы в блоках ---
run_test "(x = 5; x += 3; x *= 2; x -= 4; x)" "12" "Блок с составными присваиваниями"
run_test "(x = 10; y = 5; x += y--; x)" "15" "Составное присваивание с постфиксным декрементом"
run_test "(x = 1; y = ++x + x++ + ++x; y)" "8" "Блок со сложными инкрементами"

rm -f "$TMP_OUT" "$TMP_ERR"

echo ""
echo -e "${YELLOW}=== Результат: пройдено $PASSED, не пройдено $FAILED ===${RESET}"

if [ $FAILED -gt 0 ]; then
    echo ""
    echo -e "${RED}Список проваленных тестов:${RESET}"
    for i in "${!FAILED_TESTS[@]}"; do
        echo "  ✗ ${FAILED_TESTS[$i]}"
        echo "    Выражение: ${FAILED_EXPR[$i]}"
    done
    echo ""
    echo -e "${RED}Есть ошибки, требуется доработка.${RESET}"
    exit 1
else
    echo -e "${GREEN}Все $PASSED тестов успешно пройдены! reval стабилен.${RESET}"
    exit 0
fi
