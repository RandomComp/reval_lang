#!/bin/bash

# test_reval.sh — расширенные тесты для reval

REVAL="./reval_linux_x86"
TMP_OUT="test_output.tmp"
TMP_ERR="test_error.tmp"

# Цвета
GREEN="\033[32m"
RED="\033[31m"
RESET="\033[0m"

PASSED=0
FAILED=0

run_test() {
    local expr="$1"
    local expected="$2"
    local description="$3"
    local expect_error="$4"

    echo "Ожидается $expr = $expected"

    "$REVAL" "$expr" > "$TMP_OUT" 2> "$TMP_ERR"
    local result=$?
    local output=$(cat "$TMP_OUT" | tr -d '\n\r')
    local error_output=$(cat "$TMP_ERR" | sed '/^$/d')

    if [ -n "$expect_error" ]; then
        if [ $result -ne 0 ] || [ -n "$error_output" ]; then
            echo "Результат: ошибка (код $result)"
            if [ -n "$error_output" ]; then
                echo "stderr:"
                echo "$error_output"
            fi
            echo -e "${GREEN}✓${RESET} $description"
            ((PASSED++))
        else
            echo "Результат: $output (код 0, но ожидалась ошибка)"
            echo -e "${RED}✗${RESET} $description"
            ((FAILED++))
        fi
    else
        if [ "$output" == "$expected" ]; then
            echo "Результат: $output"
            echo -e "${GREEN}✓${RESET} $description"
            ((PASSED++))
        else
            echo "Результат: $output (ожидалось $expected)"
            if [ -n "$error_output" ]; then
                echo "stderr:"
                echo "$error_output"
            fi
            echo -e "${RED}✗${RESET} $description"
            ((FAILED++))
        fi
    fi
    echo ""
}

if [ ! -f "$REVAL" ]; then
    echo "Не найден $REVAL, запусти make"
    exit 1
fi

echo "=== Расширенное тестирование reval ==="
echo ""

# --- Базовые операции (уже были) ---
run_test "2 + 2" "4" "Простая арифметика"
run_test "10 - 3" "7" "Вычитание"
run_test "3 * 4" "12" "Умножение"
run_test "12 / 3" "4" "Деление"
run_test "10 % 3" "1" "Остаток от деления"
run_test "2 ** 10" "1024" "Возведение в степень"

# --- Сложные арифметические выражения ---
run_test "2 + 3 * 4 - 8 / 2" "10" "Арифметика с приоритетами"          # 2 + 12 - 4 = 10
run_test "10 - 2 * 3 ** 2 + 4" "-4" "Степень выше умножения"           # 10 - 2*9 + 4 = 10 - 18 + 4 = -4
run_test "(10 - 2) * 3 ** 2 + 4" "76" "Скобки и степень"               # 8*9+4=72+4=76
run_test "100 / 4 % 3" "1" "Деление и остаток"                        # 25%3=1
run_test "100 / (4 % 3)" "100" "Скобки меняют порядок"                # 4%3=1 -> 100/1=100

# --- Битовые и логические операции ---
run_test "~0" "-1" "Побитовое НЕ 0"
run_test "~-1" "0" "Побитовое НЕ -1"
run_test "!0" "1" "Логическое НЕ 0"
run_test "!123" "0" "Логическое НЕ ненулевого"
run_test "!!123" "1" "Двойное логическое НЕ"
run_test "~5 + 1" "-5" "Побитовое НЕ + 1 даёт -x"                     # ~5=-6, -6+1=-5
run_test "-~5" "6" "Унарный минус побитового НЕ"

# --- Сравнения и логика ---
run_test "5 > 3 && 4 < 6" "1" "Логическое И (истина)"
run_test "5 > 3 && 4 > 6" "0" "Логическое И (ложь)"
run_test "5 > 3 || 4 > 6" "1" "Логическое ИЛИ (истина)"
run_test "5 < 3 || 4 > 6" "0" "Логическое ИЛИ (ложь)"
run_test "(5 > 3) && (4 < 6)" "1" "Скобки с логикой"
run_test "(5 > 3) == (4 < 6)" "1" "Сравнение логических значений"
run_test "(5 > 3) != (4 > 6)" "1" "Неравенство логических значений"

# --- Тернарный оператор (вложенный) ---
run_test "5 > 3 ? 10 : 20" "10" "Тернарный простой (истина)"
run_test "5 < 3 ? 10 : 20" "20" "Тернарный простой (ложь)"
run_test "5 > 3 ? (6 > 4 ? 100 : 200) : 300" "100" "Вложенный тернарный (истина-истина)"
run_test "5 > 3 ? (6 < 4 ? 100 : 200) : 300" "200" "Вложенный тернарный (истина-ложь)"
run_test "5 < 3 ? (6 > 4 ? 100 : 200) : 300" "300" "Вложенный тернарный (ложь)"
run_test "(5 > 3) ? (10) : (20)" "10" "Тернарный с выражениями в ветках"

# --- Сложные комбинации ---
run_test "(5 + 3) * 2 > 10 ? 100 : 200" "100" "Тернарный с вычислениями"
run_test "(5 + 3) * 2 < 10 ? 100 : 200" "200" "Тернарный с вычислениями"
run_test "!(5 > 3)" "0" "Логическое НЕ сравнения"
run_test "!!(5 > 3)" "1" "Двойное логическое НЕ сравнения"
run_test "~5 == -6" "1" "Сравнение побитового НЕ"
run_test "!5 == 0" "1" "Сравнение логического НЕ"

# --- Разные системы счисления в выражениях ---
run_test "0xFF + 0b1111" "270" "Hex + bin"                           # 255 + 15 = 270
run_test "0o77 * 2" "126" "Oct * 2"                                  # 63*2=126
run_test "0xFF - 0o77" "192" "Hex - Oct"                             # 255-63=192
run_test "0b1010 ** 2" "100" "Bin степень"                           # 10^2=100

# --- Переменные и присваивание (если уже реализовано) ---
run_test "x = 5; x" "5" "Присваивание и чтение"
run_test "x = 5; y = x * 2; y + 3" "13" "Несколько присваиваний"
run_test "x = 10; x = x + 2; x" "12" "Инкремент через присваивание"
run_test "x = 5; y = 10; x > y ? x : y" "10" "Тернарный с переменными"

# --- Блоки и последовательности ---
run_test "(x = 5; y = x * 2; y)" "10" "Блок возвращает последнее"
run_test "(x = 2; y = 3; x * y)" "6" "Блок с умножением"
run_test "(x = 2; y = 3; x * y) + 1" "7" "Блок в выражении"

# --- Крайние случаи ---
run_test "0 ** 0" "1" "Ноль в нулевой степени (условность)"
run_test "1 / 1" "1" "Деление единицы"
run_test "0 % 5" "0" "Ноль при остатке"
run_test "5 % 0" "" "Ошибка: остаток от деления на ноль" "1"
run_test "(-(-(-5)))" "-5" "Тройной унарный минус"
run_test "~(~(~5))" "-5" "Тройное побитовое НЕ"
run_test "!!!!!!!!5" "0" "Много логических НЕ (чётное = положительное, нечётное = отрицательное?)"  # 8 ! = 0
run_test "----------------------------------------5" "5" "40 унарных минусов"
run_test "((((((((((((((((((((5))))))))))))))))))))" "5" "Много скобок"

# --- Ошибки (синтаксис и семантика) ---
run_test "1 / 0" "" "Ошибка: деление на ноль" "1"
run_test "0 ** 0" "1" "Ноль в нулевой степени (условность)"  # не ошибка, а предупреждение
run_test "(2 + 2" "" "Ошибка: незакрытая скобка" "1"
run_test "2 + 2)" "" "Ошибка: лишняя скобка" "1"
run_test "" "" "Ошибка: пустое выражение" "1"
run_test "2 3" "" "Ошибка: два числа подряд" "1"
run_test "x y" "" "Ошибка: два слова подряд" "1"
run_test "2 # 3" "" "Ошибка: неизвестный оператор" "1"
run_test "0x1G" "" "Ошибка: неверный hex" "1"
run_test "0b12" "" "Ошибка: неверный bin" "1"
run_test "12e3" "" "Ошибка: неверный dec" "1"
run_test "0o89" "" "Ошибка: неверный oct" "1"
run_test "2 +" "" "Ошибка: нет операнда после +" "1"
run_test "2 *" "" "Ошибка: нет операнда после *" "1"
run_test "x = ;" "" "Ошибка: нет правой части" "1"
run_test "x = 5; y = ;" "" "Ошибка: второе присваивание без правой части" "1"

rm -f "$TMP_OUT" "$TMP_ERR"

echo "=== Результат: пройдено $PASSED, не пройдено $FAILED ==="

if [ $FAILED -eq 0 ]; then
    echo -e "${GREEN}Все расширенные тесты успешно пройдены!${RESET}"
    exit 0
else
    echo -e "${RED}Есть ошибки, требуется доработка.${RESET}"
    exit 1
fi
